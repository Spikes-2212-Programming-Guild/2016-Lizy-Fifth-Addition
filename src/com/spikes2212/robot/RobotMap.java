package com.spikes2212.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	public static final int DRIVETRAIN_RIGHT_FRONT_PORT = 0;
	public static final int DRIVETRAIN_RIGHT_REAR_PORT = 0;
	public static final int DRIVETRAIN_LEFT_FRONT_PORT = 0;
	public static final int DRIVETRAIN_LEFT_REAR_PORT = 0;
	public static final int TRIZ_MOTOR_PORT = 0;
	public static final int TRIZ_UP_PORT = 0;
	public static final int TRIZ_DOWN_PORT = 0;
}
